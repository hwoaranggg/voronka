def build_pitch(answers: dict) -> str:
    """Персональная подводка к офферу на основе ответов квиза."""
    lines: list[str] = []

    fam = answers.get("familiar")
    if fam == "no":
        lines.append("Ты в нише новичок — это даже плюс: зайдёшь без вредных привычек и сразу по системе.")
    elif fam == "tiktok":
        lines.append("Ты видел чаттинг в тиктоке — теперь разберёшься, как там реально устроены деньги, а не хайп.")
    elif fam == "tried":
        lines.append("У тебя уже есть попытки — значит понимаешь, где буксует. Мануал закроет именно системные дыры.")

    income = answers.get("income")
    if income == "lt1k":
        lines.append("Цель до 1000$ — абсолютно реальна уже в первые недели стабильной работы.")
    elif income == "1k3k":
        lines.append("1000–3000$ — рабочий ориентир со 2-го месяца при дисциплине и хорошем прогреве.")
    elif income == "gt3k":
        lines.append("3000$+ — это уже про рост до старшего чаттера/тимлида и % с оборота. Путь туда есть.")

    time_ = answers.get("time")
    if time_ == "12":
        lines.append("1–2 часа — норм для старта, но для стабильного дохода планируй выходить на 4–6 часов.")
    elif time_ in ("46", "full"):
        lines.append("С таким графиком выйти на стабильный доход — вопрос дисциплины, а не удачи.")

    return "\n\n".join(lines)


